const i18n = {
    currentLang: localStorage.getItem('saju_lang') || 'ko',

    init() {
        this.applyTranslations();
    },

    setLanguage(lang) {
        this.currentLang = lang;
        localStorage.setItem('saju_lang', lang);
        this.applyTranslations();

        // Trigger a custom event for other modules
        document.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang } }));
    },

    t(key) {
        if (!TRANSLATIONS[this.currentLang]) return key;
        return TRANSLATIONS[this.currentLang][key] || key;
    },

    applyTranslations() {
        // Find all elements with data-i18n attribute
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            const translation = this.t(key);

            if (el.tagName === 'INPUT' && el.type === 'placeholder') {
                el.placeholder = translation;
            } else if (el.tagName === 'OPTION') {
                el.textContent = translation;
            } else {
                // For regular elements, check if they have a span.btn-text or similar
                const btnText = el.querySelector('.btn-text');
                if (btnText) {
                    btnText.textContent = translation;
                } else {
                    el.innerHTML = translation; // Support HTML (emojis/formatting)
                }
            }
        });

        // Update html lang attribute
        document.documentElement.lang = this.currentLang;

        // Update toggle button text if exists
        const langBtn = document.getElementById('langToggle');
        if (langBtn) {
            langBtn.textContent = this.currentLang === 'ko' ? 'EN' : 'KO';
        }
    }
};

window.i18n = i18n;
